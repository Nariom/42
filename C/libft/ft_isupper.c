#include "libft.h"

int	ft_isupper(char *str)
{
	int	i;
	int	res;

	i = 0;
	res = 1;
	while ((str[i] != '\0') && (res == 1))
	{
		if ((str[i] < 'A') || (str[i] > 'Z'))
			res = 0;
		i++;
	}
	return (res);
}
