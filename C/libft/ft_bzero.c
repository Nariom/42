#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;

	if (s)
	{
		tmp = (char *)s;
		while (n > 0)
		{
			*tmp = (char) 0;
			tmp++;
			n--;
		}
	}
}
