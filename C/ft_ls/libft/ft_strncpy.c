#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (s1 && s2)
	{
		i = 0;
		while (s2[i] && i < n)
		{
			s1[i] = s2[i];
			i++;
		}
		while (i < n)
		{
			s1[i] = '\0';
			i++;
		}
	}
	return (s1);
}
