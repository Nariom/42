#include "libft.h"

int	ft_factorial(int nb)
{
	int	res;

	if ((nb > 12) || (nb < 0))
		res = 0;
	else
	{
		res = 1;
		while (nb > 1)
		{
			res = res * nb;
			nb--;
		}
	}
	return (res);
}
