#include "libft.h"

int	ft_nb_ws(char *str)
{
	int		nb;

	nb = 0;
	while (str && *str)
	{
		if (*str == ' ' || *str == '\t' || *str == '\n')
			nb++;
		str++;
	}
	return (nb);
}
