#include "libft.h"

int	ft_islower(char *str)
{
	int	i;
	int	res;

	i = 0;
	res = 1;
	while ((str[i] != '\0') && (res == 1))
	{
		if ((str[i] < 'a') || (str[i] > 'z'))
			res = 0;
		i++;
	}
	return (res);
}
